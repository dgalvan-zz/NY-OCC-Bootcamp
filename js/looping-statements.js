//looping statements - execute repetitive taks 
//loops: for loop
for (let i = 1; i<=5; i++ ){
    console.log("count", i);
}

//loops: while loop
let count =1;
while (count<=5){
    console.log("while count:", count)
}

//loops: do-whiel loop
let counter = 1;
do {
    console.log("do while loop", counter);
    counter++;
} while(counter<=5);